#include "libft.h"

int			ft_is_numeric(char *str)
{
	size_t		i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return ((i > 0) ? 1 : 0);
}
